def get_serialize_code(param: dict, all_params: list[dict]) -> str:
    """Generate serializer code for a single parameter."""
    kind = param.get("kind", "value")
    name = param["name"]
    ptype = param["type"]

    # Handle special cases with external count/size parameters
    if kind == "raw_ptr":
        # Find the size parameter (usually named "size" or "dataSize")
        size_param = None
        for p in all_params:
            if p["name"].lower().find("size") >= 0 and p.get("kind") == "value":
                size_param = p["name"]
                break
        if size_param:
            return f'ser.write_raw({name}, {size_param});'
        return f'ser.write_raw({name}, 0);  // raw pointer (unknown size)'

    if kind == "array":
        count_param = None
        # Find count: look for the NEAREST uint32_t value param preceding this one
        param_idx = next((i for i, p in enumerate(all_params) if p["name"] == name), -1)
        for j in range(param_idx - 1, -1, -1):
            p = all_params[j]
            if p.get("kind") == "value" and p["type"] == "uint32_t":
                count_param = p["name"]
                break
        if not count_param:
            count_param = name[1:] + 'Count' if name.startswith('p') else name + 'Count'
        return f'ser.write_array({name}, {count_param});'

    if kind == "handle":
        return f'ser.write_handle(reinterpret_cast<uint64_t>({name}));'

    if kind == "output_handle_ptr":
        return f'ser.write_handle(reinterpret_cast<uint64_t>({name}));'

    if kind == "value":
        # Check if it's a float type
        if ptype in ("float", "VkFloat"):
            return f'ser.write_f32({name});'
        # VkBool32
        if ptype == "VkBool32":
            return f'ser.write_bool({name});'
        # Default: uint32_t
        return f'ser.write_u32(static_cast<uint32_t>({name}));'

    if kind == "value_ptr":
        return f'ser.write_u32({name} ? *{name} : 0);'

    if kind == "struct_ptr":
        return f'ser.write_raw({name}, sizeof(*{name}));'

    if kind == "string":
        return f'ser.write_string({name});'

    return f'ser.write_raw(&{name}, sizeof({name}));  // fallback'


def generate(
    functions: list[dict[str, Any]],
    source: str,
    output_path: Path,
) -> None:
    """Render the C++ intercept file from template."""
    if jinja2 is None:
        print("ERROR: jinja2 is required. Install with: pip install -r gen/requirements.txt", file=sys.stderr)
        sys.exit(1)

    def serialize_param(param, loop):
        return get_serialize_code(param, loop)

    env = jinja2.Environment(
        loader=jinja2.BaseLoader(),
        trim_blocks=True,
        lstrip_blocks=True,
    )
    env.globals["serialize_param"] = serialize_param

    template = env.from_string(INIT_TEMPLATE_RENAMED)

    rendered = template.render(
        source=source,
        timestamp=datetime.now().isoformat(),
        functions=functions,
    )

    output_path.parent.mkdir(parents=True, exist_ok=True)
    output_path.write_text(rendered, encoding="utf-8")

    print(f"Generated {output_path} with {len(functions)} intercepted functions")


def parse_manifest(manifest_path: Path) -> list[dict[str, Any]]:
    """Read API manifest from JSON file."""
    with open(manifest_path, encoding="utf-8") as f:
        data = json.load(f)
    return data.get("functions", [])


def main() -> None:
    parser = argparse.ArgumentParser(description="OmniGPU Vulkan Interception Code Generator")
    parser.add_argument("--manifest", type=Path, default="gen/vulkan_api.json",
                        help="Path to JSON API manifest")
    parser.add_argument("--output", type=Path, required=True, help="Output C++ file path")
    parser.add_argument("--list", action="store_true", help="List parsed functions and exit")
    args = parser.parse_args()

    if not args.manifest.exists():
        parser.error(f"Manifest not found: {args.manifest}")

    functions = parse_manifest(args.manifest)
    source = str(args.manifest)

    if args.list:
        for func in functions:
            params = ", ".join(f"{p['type']} {p['name']} [{p.get('kind', '?')}]" for p in func["params"])
            print(f"{func['return_type']} {func['name']}({params})")
        print(f"\nTotal: {len(functions)} functions")
        return

    generate(functions, source, args.output)


if __name__ == "__main__":
    main()
